function sum(a, b) {
    var sum = a + b;
    return sum;
}

function subtraction(a, b) {
    var subValue = a - b;
    return subValue;
}

function multiplication(a, b) {
    var multipValue = a * b;
    return multipValue;
}


function division(a, b) {
    var divValue = a / b;
    return divValue;
}

module.exports = {sum, subtraction, multiplication, division};